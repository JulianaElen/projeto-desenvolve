# Módulo para criar e imprimir o labirinto.

# Pelo menos duas funções: criar_labirinto() e imprimir_labirinto(). O labirinto pode, por exemplo, ser lido de arquivo ou gerado aleatoriamente. Mas não pode ser uma string ou outra estrutura pré-escrita no script. 

# Solte sua criatividade. Que tal colocar items no labirinto para o jogador coletar? Isso exigiria uma checagem se o jogador e o item estão na mesma posição.

# Função criar_labirinto(): Esta função deve gerar um labirinto aleatório ou ler um labirinto de um arquivo. O labirinto pode ser representado como uma matriz ou lista de listas.

# Função imprimir_labirinto(): Responsável por imprimir o labirinto no terminal de forma visual, destacando a posição do jogador e outros elementos, como itens.